function setSearchResults() {
	// Monta o paginador
	$('#paginator').empty();
	$('#results-table tbody').empty();
	var total = boletoListarResponse[0].total_de_registros;
	var page = 0; // Páginas
	var j = 0;    // Registros

	if(total > 0)
		addPreviousLink();

	for(var i = 0; i < total; i++) {
		if(j == 5) {
			j = 0;
			page++;

			addPaginatorPage(page);

		}

		j++;

	}

	if(total > 0) {
		addNextLink(2);
		toPage(1);
		$('#total-results').html('Total ' + total + ' resultados');

	}

	// Exibe os resultados na tabela
	j = 0;
	page = j + 1;
	$.each(boletoListarResponse, function() {
		var boletos = this.boletos;
		var resultPage = this.pagina;

		$.each(boletos, function() {
			if(j == 5) {
				j = 0;
				page++;

			}

			var dadosPagador = this.DadosPagador;
			var numero = this.cNumero;
			var nome = dadosPagador.cNome;
			var datadocumento = this.dDataDoc;
			var valor = this.nValorDoc;
			addResultLine(resultPage, page, numero, nome, datadocumento, valor);

			j++;

		});

	});

	// Página 1
	toPage(1);

	// Oculta todas as páginas do paginador
	$.each($('#paginator li'), function() {
		if($(this).data('link') != true)
			$(this).hide();

	});

	// Exibe as 4 primeiras páginas
	showPaginatorNumber(1);
	showPaginatorNumber(2);
	showPaginatorNumber(3);
	showPaginatorNumber(4);

}

function addPaginatorPage(page) {
	$('#paginator').append('<li class="page-item" data-page="' + page + '"><a class="page-link" href="#" onclick="toPage(' + page + ')">' + page + '</a></li>');

}

function addPreviousLink() {
	$('#paginator').append('<li class="page-item disabled" data-link="true"><a class="page-link" href="#" tabindex="-1">Anterior</a></li>');

}

function addNextLink(page) {
	$('#paginator').append('<li class="page-item" data-link="true"><a class="page-link" id="next-link" href="#" onclick="next(' + page + ')">Próximo</a></li><li class="page-item" data-link="true"><a class="page-link" href="#">Gerar boletos</a></li>');

}

function toPage(page) {
	$.each($('#results-table tbody tr'), function() {
		if($(this).data('page') != page)
			$(this).hide();

		else
			$(this).show();

	});

	// Desabilitar página

}

function showPaginatorNumber(page) {
	$.each($('#paginator li'), function() {
		if($(this).data('page') == page && $(this).data('link') != true)
			$(this).show();

	});

}

